let person = {
    name:       'Bob',
    occupation: 'web developer',
    hobbies:    'painting',
};
// Cách 1: Sử dụng dấu chấm - ObjectName.Property
console.log(person.name);
console.log(person.occupation);
console.log(person.hobbies);

// Cách 2: Sử dụng ngoặc vuông - ObjectName["Property"]
console.log(person["name"]);
console.log(person["occupation"]);
console.log(person["hobbies"]);
